# Calcular a área do tringulo

b = int(input("Informe o valor da base (b) do trinagulo: "))
h = int(input("Informe o valor da altura (h) do triangulo: "))

print("A area do triangulo é da calculada pela formula A = b*h/2")

A = (b*h)/2

print("Se a b =", b, ", altura h =", h, "são essas, então a área é A =", A)
